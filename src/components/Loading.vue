<script>
    $(function(){ //先執行使得mask覆蓋整個頁面
         
    })

    function initMask(){ //先執行使得mask覆蓋整個頁面
        var doc=document.documentElement,
            mask=document.getElementById('mask');
        var relHeight=(doc.clientHeight>doc.scrollHeight)?doc.clientHeight:doc.scrollHeight,//獲取螢幕高度和頁面高度中的較大值 
            relWidth=(doc.clientWidth>doc.scrollWidth)?doc.clientWidth:doc.scrollWidth;
        mask.style.height=relHeight+'px';
        mask.style.width=relWidth+'px';
    }

    export default {
        name: "loading_page", 
        mounted(){
            initMask();
            window.onresize=initMask;//監聽視窗大小變化，解決瀏覽器從小變大時，出現空白的bug 
        },
        methods: {
            showLoading:function() {
                $('#loading').show();
            },
            hideLoading:function() {
                $('#loading').hide();
            }
        }
    }
</script>

